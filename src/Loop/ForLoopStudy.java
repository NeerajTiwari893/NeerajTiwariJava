package Loop;

public class ForLoopStudy {

	public static void main(String[] args) {
		
		// For Loop condition
		//intialisation - 0 to 5,0,1,2,3,4,5, a++ - // 90 to 100
		//condition - a<=5
		//updation - a+1
 // if (int a;A<=5;a++)
	// For Loop 	
		
		for(int a=1;a<=5;a++)// a++ =A+1, a=1+1=2,3,4,5
		{
		System.out.println(a);
		}
	}

}
